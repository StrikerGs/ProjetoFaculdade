    private void tblProdutosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutosMousePressed
        // TODO add your handling code here:
        Point point = evt.getPoint();
        int row = tblProdutos.rowAtPoint(point);
        if (evt.getClickCount() == 2 && tblProdutos.getSelectedRow() != -1) {
            // your valueChanged overridden method 
            txtCodigo.setText(tblProdutos.getModel().getValueAt(row, 0).toString());
            txtProduto.setText(tblProdutos.getModel().getValueAt(row, 1).toString());
            txtQuantidade.setText(tblProdutos.getModel().getValueAt(row, 2).toString());
            txtPreco.setText(tblProdutos.getModel().getValueAt(row, 3).toString());

            btnCadastrar.setEnabled(false);
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            txtCodigo.setEditable(false);
        }
    }//GEN-LAST:event_tblProdutosMousePressed

