    private boolean validaCampos() {

        if (txtCodigo.getText().equals("")) {
            txtCodigo.requestFocus();
            return false;
        }
        if (txtProduto.getText().equals("")) {
            txtProduto.requestFocus();
            return false;
        }
        if (txtQuantidade.getText().equals("")) {
            txtQuantidade.requestFocus();
            return false;
        }
        if (txtPreco.getText().equals("")) {
            txtPreco.requestFocus();
            return false;
        }
        return true;
    }

    private void limparCampos() {
        txtCodigo.setText("");
        txtProduto.setText("");
        txtQuantidade.setText("");
        txtPreco.setText("");

        btnCadastrar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        txtCodigo.setEditable(true);
    }
    
    private void preencherTabela() {
        preencherTabela("");
    }

    private void preencherTabela(String nome) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"CODIGO", "NOME", "QTD", "PREÇO"};

        ArrayList<ProductModel> produtos = null;
        if(nome.equals("")) {
            produtos = controller.listaProdutos();
        } else {
            produtos = controller.buscaProduto(nome);
        }

        for (Iterator<ProductModel> iterator = produtos.iterator(); iterator.hasNext();) {
            ProductModel p = iterator.next();
            dados.add(new Object[]{
                p.getCod(),
                p.getNome(),
                p.getQuant(),
                p.getPreco()
            });
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tblProdutos.setModel(modelo);
        tblProdutos.getColumnModel().getColumn(1).setPreferredWidth(180);
        tblProdutos.setAutoResizeMode(tblProdutos.AUTO_RESIZE_OFF);
        tblProdutos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }


